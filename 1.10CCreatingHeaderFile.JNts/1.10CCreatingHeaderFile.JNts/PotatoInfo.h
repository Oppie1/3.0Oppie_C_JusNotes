//#define creates a constant - similar to a variable, but its value can never
//be changed once set. Think of it as a fixed label for a value.

//Naming convention: Constants are written in ALL_CAPS so they stand out visually
//from regular  variables, making your code easier to read at a glance.

//What is a preprocessor directive?
//Lines that start with # (like #define and #include) are preprocessor directives.
//They are processed by the compiler before your actual program code runs,
//which is why constants defined here are ready to use the moment main() starts.
//The header file runs before your main file. It contains variables and often 
//function prototypes that main and .c files use. They all work together.
